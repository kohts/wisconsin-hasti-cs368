package cs368JavaTutorial;

public class TestYourself1 {

	public static void main(String[] args) {
		
		int x = 10;
		int y = 20;
		
		System.out.println(x + y);
		System.out.println(x + y + "!");
		System.out.println("printing: " + x + y);
		System.out.println("printing: " + (-1) * x + y);
		System.out.println("printing: " + (x + y));
		System.out.println("printing: " + x * y);
	}

}
